  const freeShippingConditions = {
      productA: ["100","250","500", "1000","5000","10000"],  // Free shipping for 500 gm and 1 kg
      productB: ["90","450","900"]          // Free shipping for 1 kg only
  };
  
  // Event Listener for Product Category Change
  productCategory.addEventListener("change", function() {
      quantityOption.value = "";
      customQuantityDiv.style.display = "none";
      selectedQuantity.innerText = "None";
      priceDisplay.innerText = "-";
      freeShipping.style.display = "none";
  });
  
  // Event Listener for Quantity Option Change
  quantityOption.addEventListener("change", function() {
      const selectedProduct = productCategory.value;
      const selectedValue = quantityOption.value;
  
      if (selectedValue === "custom") {
          customQuantityDiv.style.display = "block";
          selectedQuantity.innerText = "Custom Quantity";
          priceDisplay.innerText = "-";
          freeShipping.style.display = "none";
      } else {
          customQuantityDiv.style.display = "none";
          selectedQuantity.innerText = selectedValue ? `${selectedValue} gm` : "None";
  
          if (selectedProduct && priceList[selectedProduct][selectedValue]) {
              priceDisplay.innerText = `${priceList[selectedProduct][selectedValue]} Rs`;
  
              // Check for Free Shipping
              if (freeShippingConditions[selectedProduct].includes(selectedValue)) {
                  freeShipping.style.display = "block";
              } else {
                  freeShipping.style.display = "none";
              }
          } else {
              priceDisplay.innerText = "-";
              freeShipping.style.display = "none";
          }
      }
  });
  
  // Event Listener for Custom Quantity Input
  customQuantityInput.addEventListener("input", function() {
      selectedQuantity.innerText = customQuantityInput.value ? `${customQuantityInput.value} gm` : "None";
      priceDisplay.innerText = "-";
      freeShipping.style.display = "none";
  });
  
  

  
